    private void cargarDatosObra()
    {
        // Datos generales
        txtId.setText(String.valueOf(obraAEditar.getId()));
        txtTitulo.setText(obraAEditar.getTitulo());
        txtAnio.setText(String.valueOf(obraAEditar.getAnio()));
        txtPrecio.setText(String.valueOf(obraAEditar.getPrecio()));
        
        // Configurar tipo de obra y cargar datos especÃ­ficos
        if (obraAEditar instanceof Pintura)
        {
            cargarDatosPintura((Pintura) obraAEditar);
            cambiarPanelTipoObra(true);
        }
        else if (obraAEditar instanceof Escultura)
        {
            cargarDatosEscultura((Escultura) obraAEditar);
            cambiarPanelTipoObra(false);
        }
        
    }
    
    private void cargarDatosPintura(Pintura pintura) {
        txtEstilo.setText(pintura.getEstilo());
        txtSoporte.setText(pintura.getSoporte());
        txtDimensiones2D.setText(pintura.getDimensiones2D());
    }
    
    private void cargarDatosEscultura(Escultura escultura) {
        txtMaterial.setText(escultura.getMaterial());
        txtPeso.setText(escultura.getPeso());
        txtDimensiones3D.setText(escultura.getDimensiones3D());
    }
    
    private void cambiarPanelTipoObra(boolean condicion)
    {
        if(condicion)
        {
           panelPintura.setVisible(true);
           panelEscultura.setVisible(false);
        }
        else
        {
           panelEscultura.setVisible(true);
           panelPintura.setVisible(false);
        }
        
    }
    
    private void guardarCambios()
    {
        try {
            // Validaciones bÃ¡sicas
            if (txtTitulo.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El tÃ­tulo es obligatorio");
                return;
            }
            
            if (txtId.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El ID es obligatorio");
                return;
            }
            
            if (txtAnio.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "El AÃ±o es obligatorio");
                return;
            }
            
            if (txtPrecio.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "El Precio es obligatorio");
                return;
            }
            int nuevoId = Integer.parseInt(txtId.getText().trim());
            
            // Verificar si el ID ya existe (en otras obras)
            for (ObraArte obra : listaObras) {
                if (obra != obraAEditar && obra.getId() == nuevoId) {
                    JOptionPane.showMessageDialog(this, "Ya existe otra obra con ese ID");
                    return;
                }
            }
            
            // Actualizar datos generales
            obraAEditar.setId(nuevoId);
            obraAEditar.setTitulo(txtTitulo.getText().trim());
            obraAEditar.setAnio(Integer.parseInt(txtAnio.getText().trim()));
            obraAEditar.setPrecio(Float.parseFloat(txtPrecio.getText().trim()));
            
            // Actualizar datos especÃ­ficos segÃºn el tipo
            if (obraAEditar instanceof Pintura)
            {
                Pintura pintura = (Pintura) obraAEditar;
                pintura.setEstilo(txtEstilo.getText().trim());
                pintura.setSoporte(txtSoporte.getText().trim());
                pintura.setDimensiones2D(txtDimensiones2D.getText().trim());
            }
            else if (obraAEditar instanceof Escultura)
            {
                Escultura escultura = (Escultura) obraAEditar;
                escultura.setMaterial(txtMaterial.getText().trim());
                escultura.setPeso(txtPeso.getText().trim());
                escultura.setDimensiones3D(txtDimensiones3D.getText().trim());
            }
            
            JOptionPane.showMessageDialog(this, "Obra actualizada exitosamente");
            dispose();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en formatos numÃ©ricos: verifique ID, aÃ±o y precio");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    
